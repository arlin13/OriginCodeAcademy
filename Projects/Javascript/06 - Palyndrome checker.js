function isPalindrome(text) {
	// Write your code here!
	if (text === reverseString(text))
	  return true;
	else
	  return false;
}

function reverseString(text) {
	var result = "";
	var textLength = text.length;
	// Write your code here!
  for (var i=textLength-1; i>=0; i--)
  {
    result+= text.charAt(i);
  }
	return result;
}

isPalindrome('Racecar');
isPalindrome('John');
